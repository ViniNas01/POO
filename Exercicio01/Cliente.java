public class Cliente {
    String nome;
    int idade;
    String cpf;
    String rg;
    String cidade;
    int matricula;

    Cliente(String nome, int idade, String cpf, String rg, String cidade, int matricula){
        this.nome = nome;
        this.idade = idade;
        this.cpf = cpf;
        this.rg = rg;
        this.cidade = cidade;
        this.matricula = matricula;
    }

}